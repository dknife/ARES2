# ==========================
# [클래스 정의]
# ==========================
class KSicon:
    """
    아이콘 렌더링 클래스.
    비트맵 데이터를 OLED에 표시합니다.
    """
    def __init__(self, buff, w, h, oled):
        self.w = w
        self.h = h
        self.buff = buff
        self.oled = oled
        # buff는 수평 바이트 행으로 가정: 각 바이트 = 8 수평 픽셀
        # FrameBuffer.MONO_VLSB용 수직 바이트 포맷으로 변환

        # 수평에서 수직 포맷 버퍼로 변환
        vertical_buff = bytearray(self.w * self.h // 8)
        bytes_per_row = self.w // 8

        for row in range(self.h):
            for byte_col in range(bytes_per_row):
                byte = buff[row * bytes_per_row + byte_col]
                for bit in range(8):
                    pixel_on = (byte >> (7 - bit)) & 1
                    if pixel_on:
                        px = byte_col * 8 + bit
                        py = row
                        vert_byte_index = px + (py // 8) * self.w
                        vertical_buff[vert_byte_index] |= (1 << (py % 8))

        self.fb = framebuf.FrameBuffer(vertical_buff, self.w, self.h, framebuf.MONO_VLSB)

    def blit(self, x, y):
        # 아이콘을 OLED에 그리기
        for i in range(self.w):
            for j in range(self.h):
                if self.fb.pixel(i, j):
                    self.oled.pixel(x + i, y + j, 1)
